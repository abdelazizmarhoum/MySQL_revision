-- Obtenir la date/heure actuelle
SELECT NOW();
SELECT CURDATE();
SELECT CURTIME();
SELECT CURRENT_TIMESTAMP();
SELECT CURRENT_DATE();
SELECT CURRENT_TIME();
SELECT LOCALTIME();
SELECT LOCALTIMESTAMP();

-- Extraire des parties de date/heure avec EXTRACT
SELECT EXTRACT(YEAR FROM '2023-04-14');
SELECT EXTRACT(MONTH FROM '2023-04-14');
SELECT EXTRACT(DAY FROM '2023-04-14');
SELECT EXTRACT(HOUR FROM '14:34:56');
SELECT EXTRACT(MINUTE FROM '14:34:56');
SELECT EXTRACT(SECOND FROM '14:34:56');

-- Extraire avec les fonctions spécifiques
SELECT YEAR('2023-04-14');
SELECT MONTH('2023-04-14');
SELECT DAY('2023-04-14');
SELECT HOUR('14:34:56');
SELECT MINUTE('14:34:56');
SELECT SECOND('14:34:56');
SELECT MONTHNAME('2023-04-14');
SELECT DAYNAME('2023-04-14');

-- Ajouter des périodes avec DATE_ADD
SELECT DATE_ADD('2023-04-14', INTERVAL 1 MONTH);
SELECT DATE_ADD('2023-04-14', INTERVAL 1 DAY);
SELECT DATE_ADD('14:34:56', INTERVAL 1 HOUR);
SELECT DATE_ADD('14:34:56', INTERVAL 1 MINUTE);
SELECT DATE_ADD('14:34:56', INTERVAL 1 SECOND);

-- Soustraire des périodes avec DATE_SUB
SELECT DATE_SUB('2023-04-14', INTERVAL 1 MONTH);
SELECT DATE_SUB('2023-04-14', INTERVAL 1 DAY);
SELECT DATE_SUB('14:34:56', INTERVAL 1 HOUR);
SELECT DATE_SUB('14:34:56', INTERVAL 1 MINUTE);
SELECT DATE_SUB('14:34:56', INTERVAL 1 SECOND);

-- Différences entre dates
SELECT DATEDIFF('2023-04-14', '2023-04-01');
SELECT DATEDIFF('2023-04-14', '2023-05-14');

-- Différences entre heures
SELECT TIMEDIFF('14:34:56', '14:00:00');
SELECT TIMEDIFF('14:34:56', '15:00:00');

-- Différences entre timestamps
SELECT TIMESTAMPDIFF(MONTH, '2023-04-14', '2023-05-14');
SELECT TIMESTAMPDIFF(DAY, '2023-04-14', '2023-04-15');
SELECT TIMESTAMPDIFF(HOUR, '14:34:56', '15:34:56');
SELECT TIMESTAMPDIFF(MINUTE, '14:34:56', '14:35:56');
SELECT TIMESTAMPDIFF(SECOND, '14:34:56', '14:34:57');

